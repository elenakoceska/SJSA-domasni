function SpeedLimits(speed) {
    if (speed <= 50) {
        return ("Safe driving")
    } else if (speed > 50, speed < 120) {
        return (`Speed Limit Crossed. Penalty points:`)
    } else if (speed > 120) {
        return ("License Suspended")
    };
};
